<script lang="ts">
	let { currentPage, totalPages, totalElements, pageSize, onPageChange } = $props<{
		currentPage: number;
		totalPages: number;
		totalElements: number;
		pageSize: number;
		onPageChange: (page: number) => void;
	}>();

	function getPageNumbers(): number[] {
		const pages: number[] = [];
		const maxVisible = 5;

		if (totalPages <= maxVisible) {
			for (let i = 1; i <= totalPages; i++) {
				pages.push(i);
			}
		} else {
			if (currentPage <= 3) {
				for (let i = 1; i <= 4; i++) {
					pages.push(i);
				}
				pages.push(totalPages);
			} else if (currentPage >= totalPages - 2) {
				pages.push(1);
				for (let i = totalPages - 3; i <= totalPages; i++) {
					pages.push(i);
				}
			} else {
				pages.push(1);
				for (let i = currentPage - 1; i <= currentPage + 1; i++) {
					pages.push(i);
				}
				pages.push(totalPages);
			}
		}

		return pages;
	}
</script>

{#if totalPages > 1}
	<div
		class="flex items-center justify-between border-t border-gray-200 bg-white px-4 py-3 sm:px-6"
	>
		<div class="flex flex-1 justify-between sm:hidden">
			<button
				onclick={() => onPageChange(currentPage - 1)}
				disabled={currentPage === 1}
				class="relative inline-flex items-center rounded-md border border-gray-300 bg-white px-4 py-2 text-sm font-medium text-gray-700 hover:bg-gray-50 disabled:cursor-not-allowed disabled:opacity-50"
			>
				Anterior
			</button>
			<button
				onclick={() => onPageChange(currentPage + 1)}
				disabled={currentPage === totalPages}
				class="relative ml-3 inline-flex items-center rounded-md border border-gray-300 bg-white px-4 py-2 text-sm font-medium text-gray-700 hover:bg-gray-50 disabled:cursor-not-allowed disabled:opacity-50"
			>
				Siguiente
			</button>
		</div>
		<div class="hidden sm:flex sm:flex-1 sm:items-center sm:justify-between">
			<div>
				<p class="text-sm text-gray-700">
					Mostrando <span class="font-medium">{(currentPage - 1) * pageSize + 1}</span> a
					<span class="font-medium">{Math.min(currentPage * pageSize, totalElements)}</span>
					de <span class="font-medium">{totalElements}</span> resultados
				</p>
			</div>
			<div>
				<nav class="isolate inline-flex -space-x-px rounded-md shadow-sm" aria-label="Pagination">
					<button
						onclick={() => onPageChange(currentPage - 1)}
						disabled={currentPage === 1}
						class="relative inline-flex items-center rounded-l-md px-2 py-2 text-gray-400 ring-1 ring-gray-300 ring-inset hover:bg-gray-50 focus:z-20 focus:outline-offset-0 disabled:cursor-not-allowed disabled:opacity-50"
					>
						<span class="sr-only">Anterior</span>
						<svg class="h-5 w-5" viewBox="0 0 20 20" fill="currentColor" aria-hidden="true">
							<path
								fill-rule="evenodd"
								d="M12.79 5.23a.75.75 0 01-.02 1.06L8.832 10l3.938 3.71a.75.75 0 11-1.04 1.08l-4.5-4.25a.75.75 0 010-1.08l4.5-4.25a.75.75 0 011.06.02z"
								clip-rule="evenodd"
							/>
						</svg>
					</button>

					{#each getPageNumbers() as pageNum}
						{#if pageNum === 1 && getPageNumbers()[0] !== 1}
							<button
								onclick={() => onPageChange(1)}
								class="relative inline-flex items-center px-4 py-2 text-sm font-semibold text-gray-900 ring-1 ring-gray-300 ring-inset hover:bg-gray-50 focus:z-20 focus:outline-offset-0"
							>
								1
							</button>
							{#if getPageNumbers()[1] !== 2}
								<span
									class="relative inline-flex items-center px-4 py-2 text-sm font-semibold text-gray-700 ring-1 ring-gray-300 ring-inset focus:outline-offset-0"
									>...</span
								>
							{/if}
						{:else if pageNum === totalPages && getPageNumbers()[getPageNumbers().length - 1] !== totalPages}
							{#if getPageNumbers()[getPageNumbers().length - 2] !== totalPages - 1}
								<span
									class="relative inline-flex items-center px-4 py-2 text-sm font-semibold text-gray-700 ring-1 ring-gray-300 ring-inset focus:outline-offset-0"
									>...</span
								>
							{/if}
							<button
								onclick={() => onPageChange(totalPages)}
								class="relative inline-flex items-center px-4 py-2 text-sm font-semibold text-gray-900 ring-1 ring-gray-300 ring-inset hover:bg-gray-50 focus:z-20 focus:outline-offset-0"
							>
								{totalPages}
							</button>
						{:else}
							<button
								onclick={() => onPageChange(pageNum)}
								class="relative inline-flex items-center px-4 py-2 text-sm font-semibold {pageNum ===
								currentPage
									? 'z-10 bg-blue-600 text-white focus-visible:outline focus-visible:outline-2 focus-visible:outline-offset-2 focus-visible:outline-blue-600'
									: 'text-gray-900 ring-1 ring-gray-300 ring-inset hover:bg-gray-50 focus:z-20 focus:outline-offset-0'} ring-1 ring-gray-300 ring-inset"
							>
								{pageNum}
							</button>
						{/if}
					{/each}

					<button
						onclick={() => onPageChange(currentPage + 1)}
						disabled={currentPage === totalPages}
						class="relative inline-flex items-center rounded-r-md px-2 py-2 text-gray-400 ring-1 ring-gray-300 ring-inset hover:bg-gray-50 focus:z-20 focus:outline-offset-0 disabled:cursor-not-allowed disabled:opacity-50"
					>
						<span class="sr-only">Siguiente</span>
						<svg class="h-5 w-5" viewBox="0 0 20 20" fill="currentColor" aria-hidden="true">
							<path
								fill-rule="evenodd"
								d="M7.21 14.77a.75.75 0 01.02-1.06L11.168 10 7.23 6.29a.75.75 0 111.04-1.08l4.5 4.25a.75.75 0 010 1.08l-4.5 4.25a.75.75 0 01-1.06-.02z"
								clip-rule="evenodd"
							/>
						</svg>
					</button>
				</nav>
			</div>
		</div>
	</div>
{/if}
